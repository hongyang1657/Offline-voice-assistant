public class ShareAccountBean {

    private String wifiAccount;
    private String wifiPwd;
    private String ayahAccount;
    private String ayahPwd;

    public String getWifiAccount() {
        return wifiAccount;
    }

    public void setWifiAccount(String wifiAccount) {
        this.wifiAccount = wifiAccount;
    }

    public String getWifiPwd() {
        return wifiPwd;
    }

    public void setWifiPwd(String wifiPwd) {
        this.wifiPwd = wifiPwd;
    }

    public String getAyahAccount() {
        return ayahAccount;
    }

    public void setAyahAccount(String ayahAccount) {
        this.ayahAccount = ayahAccount;
    }

    public String getAyahPwd() {
        return ayahPwd;
    }

    public void setAyahPwd(String ayahPwd) {
        this.ayahPwd = ayahPwd;
    }
}
